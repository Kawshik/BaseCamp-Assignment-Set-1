package utils;

public class Printer {
	public static void printArray(String[] words) {
		for (int i = 0; i < words.length; i++) {
			System.out.println(words[i]);
		}
	}

	public static void printArray(char[] words) {
		for (int i = 0; i < words.length; i++) {
			System.out.println(words[i]);
		}
	}

	public static void printArray(int[] words) {
		for (int i = 0; i < words.length; i++) {
			System.out.println(words[i]);
		}
	}

	public static void printArray(float[] words) {
		for (int i = 0; i < words.length; i++) {
			System.out.println(words[i]);
		}
	}

	public static void printArray(double[] words) {
		for (int i = 0; i < words.length; i++) {
			System.out.println(words[i]);
		}
	}

	public static void printArray(long[] words) {
		for (int i = 0; i < words.length; i++) {
			System.out.println(words[i]);
		}
	}
}
